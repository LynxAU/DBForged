def _damage(base, kind="ki", **scaling):
    data = {
        "type": "damage",
        "damage_kind": kind,
        "base_damage": base,
        "scaling": {
            "base": base,
            "strength": scaling.get("strength", 0.2),
            "speed": scaling.get("speed", 0.1),
            "mastery": scaling.get("mastery", 0.2),
            "ki_control": scaling.get("ki_control", 0.2),
            "pl": scaling.get("pl", 0.0018),
            "power_level_hook": scaling.get("power_level_hook", "standard"),
        },
    }
    if scaling.get("notes"):
        data["scaling"]["notes"] = scaling["notes"]
    return data


def _tech(
    key,
    name,
    category,
    desc,
    tags,
    ki_cost,
    cooldown,
    *,
    effect=None,
    cast_time=0.0,
    range_band="mid",
    target="enemy",
    prerequisites=None,
    extras=None,
    vfx_id=None,
    aliases=None,
):
    effect = effect or {"type": "utility", "summary": "No effect data"}
    scaling = effect.get("scaling", {}) if isinstance(effect, dict) else {}
    return (
        key,
        {
            "name": name,
            "display_name": name,
            "description": desc,
            "category": category,
            "tags": list(tags),
            "resource_costs": {"ki": int(ki_cost)},
            "ki_cost": int(ki_cost),  # backwards-compatible convenience field
            "cooldown": float(cooldown),
            "cast_time": float(cast_time),
            "base_effect_magnitude": effect.get("base_damage") if effect.get("type") == "damage" else effect,
            "effect": effect,
            "scaling": scaling,
            "target_rules": {"target": target, "range": range_band},
            "prerequisites": prerequisites or {},
            "context_extras": extras or {},
            "vfx_id": vfx_id or f"vfx_{key}",
            "aliases": aliases or [],
            "ui_summary": summarize_effect({"effect": effect, "scaling": scaling, "tags": tags}),
            "framework": "Technique",
            "ready_state": "stub_integration_ready",
        },
    )


