def get_technique(key_or_name):
    return find_by_key_or_name(TECHNIQUES, key_or_name)


def is_beam(tech_key):
    return "beam" in TECHNIQUES.get(tech_key, {}).get("tags", [])


def get_technique_categories():
    ordered = ["melee", "ki", "beam", "control", "utility", "defense", "movement", "transformation"]
    return ordered


def iter_techniques():
    for key in sorted(TECHNIQUES.keys()):
        yield key, TECHNIQUES[key]


def execute_technique_stub(caller, technique_key, target=None, context=None):
    """
    Placeholder executor used by commands and tests before combat resolution is finalized.
    """
    if technique_key not in TECHNIQUES:
        raise KeyError(f"Unknown technique: {technique_key}")
    technique = TECHNIQUES[technique_key]
    return make_stub_result(
        "technique",
        technique_key,
        caller=caller,
        target=target,
        payload={
            "effect": technique.get("effect", {}),
            "scaling": technique.get("scaling", {}),
            "context": context or {},
        },
    )


def validate_technique_registry():
    required_fields = {
        "id",
        "name",
        "description",
        "tags",
        "resource_costs",
        "cooldown",
        "effect",
        "target_rules",
        "prerequisites",
    }
    errors = []
    for key, data in TECHNIQUES.items():
        missing = sorted(required_fields - set(data.keys()))
        if missing:
            errors.append(f"{key}: missing {', '.join(missing)}")
        if "ki" not in data.get("resource_costs", {}):
            errors.append(f"{key}: resource_costs.ki missing")
    return errors
